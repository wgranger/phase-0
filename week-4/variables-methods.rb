How do you define a method?

A method is defined with some code that is wrapped between a "def" tag and 
an ending tag. Those two tags create the method; however, what is included
inside is up to the coder.

What is the difference between a local variable and a method?

A local variable is simply an object that is defined with an equals sign.
These variables can then be used or refered to throughout the program.
A method is a bit of code which might be repeated later in the program.
A method might use variables or have several different lines of code contained
within.

How do you run a ruby program from the command line?

Ruby programs are run similarly to how git is run. To run a Ruby program, 
the user must enter "ruby program_name.rb" in the command line, with .rb 
being the standard file extension for Ruby files.

How do you run an RSpec file from the command line?

To run a test, RSpec file from the command line, the user must enter 
"rspec file_name.rb" from the command line. The terminal will then run 
the test and let the user know where the problems exist in the code which 
was written.

